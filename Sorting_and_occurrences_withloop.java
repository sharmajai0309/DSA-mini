    public static void sorting(int[] ar) {
        for (int i = 0; i < ar.length; i++) {
            int j = i;
            while (j > 0 && ar[j] < ar[j - 1]) {
                int data = ar[j];
                ar[j] = ar[j - 1];
                ar[j - 1] = data;
                j--;
            }
        }
    }

    public static int countOccurrences(int[] ar, int target) {
        int count = 0;
        for (int i = 0; i < ar.length; i++) {
            if (ar[i] == target) {
                count++;
            } else if (ar[i] > target) {
                break;
            }

        }
        return count;

    }

